public class IntMaths {
    public static int add() {
        int hello = 10;
        return hello + 33;
    }

    public static int multiply() {
        int hello = 10;
        return hello * 3;
    }

    public static int divide() {
        int hello = 10;
        return hello / 3;
    }

    public static int minus() {
        int hello = 10;
        return hello - 3;
    }

    public static int neg() {
        int hello = 10;
        return -hello;
    }


    public static void main(String[] args) {
        add();
    }
}
